#include<stdio.h>
int main()
{
	int t,n,m,a[500],c[500],count,i,j,k,x,f2,flag,d[500],y,l,z;
	scanf("%d",&t);
	while(t--)
	{
		count=0;
		for(i=0;i<500;i++)
		{
			c[i]=-1;
			d[500]=-1;
		}
		scanf("%d%d",&n,&m);
		for(i=1;i<=m;i++)
		{
			scanf("%d",&a[i]);
		}
		for(i=1;i<=m;i++)
		{
			l=0;
			flag=0;
			for(k=1;k<=n;k++)
			{
				if(c[k]==a[i])
				{
					flag=2;
					f2=0;
						for(z=i+1;z<=m;z++)
						{
							if(a[z]==a[i])
							{
								d[a[i]]=z;
								f2=1;
								break;
							}
						}
						if(f2==0)
							d[a[i]]=0;
					break;
				}
			}
			if(flag==0)
			{
				for(j=1;j<=n;j++)
				{
					if(c[j]==-1)
					{
						count++;
						c[j]=a[i];
						f2=0;
						for(z=i+1;z<=m;z++)
						{
							if(a[z]==a[i])
							{
								d[a[i]]=z;
								f2=1;
								break;
							}
						}
						if(f2==0)
							d[a[i]]=0;
						flag=1;
						break;
					}
					else
					{
						if(d[c[j]]==0)
						{
							x=j;
							flag=3;
							break;
						}
						if(l<d[c[j]])
						{
							l=d[c[j]];
							x=j;
						}
					}
				}	
				if(flag==0 || flag==3)
				{
					c[x]=a[i];
					f2=0;
					for(z=i+1;z<=m;z++)
					{
						if(a[z]==a[i])
						{
							d[a[i]]=z;
							f2=1;
							break;
						}
					}
					if(f2==0)
						d[a[i]]=0;
					count++;
				}
			}
		}
		printf("%d\n",count);
	}
	return 0;
}