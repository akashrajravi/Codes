#include<bits/stdc++.h>
using namespace std;
int main()
{
	int b,c,t,type,i,j,len,count,count_c,count_b;
	char s[100004];
	scanf("%d",&t);
	while(t--)
	{
		scanf("%d",&type);
		scanf("%s",s);
		len=strlen(s);
		b=0;
		c=0;
		for(i=0;i<len;i++)
		{
			if(s[i]=='B')
				b++;
			else
				c++;
		}
		if(b==c)
		{
			if(type==0)
			{
				count_b=0,count_c=0;
				for(i=0;i<len;i+=2)
				{
					if(s[i]=='B')
						count_b++;
					else
						count_c++;
				}
				if(count_c<count_b)
					printf("%d\n",count_c);
				else
					printf("%d\n",count_b);
			}
			else
			{
				//starting with B
				count_b=0;
				count_c=0;
				j=1;
				for(i=0;i<len;i+=2)
				{
					if(s[i]!='B')
					{
						while(s[j]=='G')
							j=j+2;
						count_b=count_b+abs(i-j);
						j=j+2;
					}
				}
				//starting with G
				j=1;
				for(i=0;i<len;i+=2)
				{
					if(s[i]!='G')
					{
						while(s[j]=='B')
							j=j+2;
						count_c=count_c+abs(i-j);
						j=j+2;
					}
				}
				if(count_c<count_b)
					printf("%d\n",count_c);
				else
					printf("%d\n",count_b);
			}
		}
		else if(b==c-1)
		{
			if(type==0)
			{
				count=0;
				for(i=0;i<len;i+=2)
					if(s[i]=='B')
						count++;
				printf("%d\n",count);
			}
			else
			{
				count=0;
				j=1;
				for(i=0;i<len;i+=2)
				{
					if(s[i]!='G')
					{
						while(s[j]=='B')
							j=j+2;
						count=count+abs(i-j);
						j=j+2;
					}
				}
				printf("%d\n",count);
			}
		}
		else if(b==c+1)
		{
			if(type==0)
			{
				count=0;
				for(i=0;i<len;i+=2)
					if(s[i]=='G')
						count++;
				printf("%d\n",count);
			}
			else
			{
				count=0;
				j=1;
				for(i=0;i<len;i+=2)
				{
					if(s[i]!='B')
					{
						while(s[j]=='G')
							j=j+2;
						count=count+abs(i-j);
						j=j+2;
					}
				}
				printf("%d\n",count);
			}
		}
		else
			printf("-1\n");
	}
	return 0;
}